  explanation: `Once a table exists, you add rows with the INSERT INTO statement. This is the most fundamental write operation in SQL.

**Single Row Insert:**
\`\`\`sql
INSERT INTO table_name (col1, col2, col3) VALUES (val1, val2, val3);
\`\`\`
Always list the column names explicitly — it protects your INSERT from breaking when someone adds a column to the table later.

**Multi-Row Insert:**
\`\`\`sql
INSERT INTO table_name (col1, col2) VALUES
  (val1a, val2a),
  (val1b, val2b),
  (val1c, val2c);
\`\`\`
Multi-row inserts are significantly faster than separate single-row inserts because they only make one round-trip to the server.

**INSERT with SELECT:**
\`\`\`sql
INSERT INTO archive_users (id, email)
SELECT id, email FROM users WHERE created_at < '2020-01-01';
\`\`\`
Copies rows from one table into another.

**INSERT IGNORE:**
\`\`\`sql
INSERT IGNORE INTO users (username, email) VALUES ('alice', 'alice@example.com');
\`\`\`
If a row with the same unique key exists, silently skips the insert (no error).

**ON DUPLICATE KEY UPDATE (Upsert):**
\`\`\`sql
INSERT INTO products (id, name, price) VALUES (1, 'Widget', 9.99)
ON DUPLICATE KEY UPDATE price = VALUES(price);
\`\`\`
If the row exists (duplicate primary/unique key), updates the specified columns instead of inserting.

**AUTO_INCREMENT behavior:**
- Do not insert into an AUTO_INCREMENT column — omit it and MySQL assigns the next value.
- Use \`LAST_INSERT_ID()\` after an INSERT to get the generated ID.`,
